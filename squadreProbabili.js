/** Chiavi normalizzate per lookup robusto (punti, accenti, trattini). */
export const NAME_ALIASES = Object.fromEntries(
  Object.entries(NAME_ALIASES_RAW).map(([k, v]) => {
    const bar = k.indexOf("|");
    const nat = k.slice(0, bar);
    const raw = k.slice(bar + 1);
    return [`${nat}|${normAliasKey(raw)}`, v];
  })
);

export function getProbabileModulo(nat) {
  return PROBABILI_RAW[nat]?.modulo || "4-3-3";
}

export function getProbabiliNomi(nat) {
  return PROBABILI_RAW[nat]?.nomi || [];
}
